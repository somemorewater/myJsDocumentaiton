//Basic if...else syntax

if (condition) {
    //code to run
} else {
    //code to run instead
}

//A real example

let shoppingDone = false;
let childsAllowance;

if (shoppingDone === true) {
  childsAllowance = 10;
} else {
  childsAllowance = 5;
}

/*
we use else if to add extra  
posibilities
 */

if (shoppingDone === true) {
  childsAllowance = 10;
} else if (childsAllowance >= childsAllowance) {
  childsAllowance = 1;
} else {
  childsAllowance = 5;
}